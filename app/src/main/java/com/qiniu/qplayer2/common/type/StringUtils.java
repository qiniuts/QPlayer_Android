    public static String toHexString(byte[] bytes) {
        StringBuilder hexString = new StringBuilder();
        for (byte b : bytes) {
            int intVal = b & 0xff;
            if (intVal < 0x10)
                hexString.append('0');

            hexString.append(Integer.toHexString(intVal));
        }
        return hexString.toString();
    }
